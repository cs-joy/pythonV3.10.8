# function - a function is basically just a collection of code which performs a specific tasks

def sayhi(name, age):
    print("Hello " + name + ", you are " + age)


sayhi("Joy", "23")
sayhi("Mike", "27")

def int_str(name, age):
    print("Hello " + name + ", you are " + str(age))

int_str("John", 30)
